package edu.neusoft.springmvc.model;

public class Role {
    private int roleID;
    private int roleName;
    
    public int getRoleID() {
      return roleID;
    }
    
    public void setRoleID(int roleID) {
      this.roleID = roleID;
    }
    
    public int getRoleName() {
      return roleName;
    }
    
    public void setRoleName(int roleName) {
      this.roleName = roleName;
    }
    
    
}
